package com.css.model;

public class TrainingProgramVO {
	int trainingId, studentIntake, trainingDuration;
	String trainingName, trainingDescription;
	public int getTrainingId() {
		return trainingId;
	}
	public void setTrainingId(int trainingId) {
		this.trainingId = trainingId;
	}
	public int getStudentIntake() {
		return studentIntake;
	}
	public void setStudentIntake(int studentIntake) {
		this.studentIntake = studentIntake;
	}
	public int getTrainingDuration() {
		return trainingDuration;
	}
	public void setTrainingDuration(int trainingDuration) {
		this.trainingDuration = trainingDuration;
	}
	public String getTrainingName() {
		return trainingName;
	}
	public void setTrainingName(String trainingName) {
		this.trainingName = trainingName;
	}
	public String getTrainingDescription() {
		return trainingDescription;
	}
	public void setTrainingDescription(String trainingDescription) {
		this.trainingDescription = trainingDescription;
	}

}
